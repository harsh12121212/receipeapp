class RecipeData {
  final String title;
  final String imagePath;
  final String rating;
  final String rated;
  final String detail;
  final String description;
  final String calories;
  final String time;

  RecipeData( {
    required this.title,
    required this.imagePath,
    required this.rating,
    required this.rated,
    required this.detail,
    required this.description,
    required this.calories,
    required this.time,
  });
}